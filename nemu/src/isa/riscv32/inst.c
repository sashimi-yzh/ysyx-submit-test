static void decode_operand(Decode *s, int *rd_, word_t *src1, word_t *src2, word_t *imm, int type) {
  uint32_t i = s->isa.inst;
  int rd  = BITS(i, 11, 7);
  int rs1 = BITS(i, 19, 15);
  int rs2 = BITS(i, 24, 20);
  if (type != TYPE_B && type != TYPE_S) destR(rd); // FIXME: should consider floating-point
  switch (type) {
#ifdef CONFIG_RVFD
    case TYPE_FR: src1FR(rs1); src2FR(rs2); destFR(rd); break;
    case TYPE_FR2R: src1FR(rs1); src2FR(rs2); destR(rd); break;
    case TYPE_R2FR: src1R(rs1); destFR(rd); break;
    case TYPE_FST: immS(); destI(*imm); src1R(rs1); src2FR(rs2); break;
    case TYPE_FLD: destFR(rd); // fall through
#endif
    case TYPE_I: immI(); src1R(rs1);     src2I(*imm); break;
    case TYPE_U: immU(); src1I(*imm);    src2I(*imm + s->pc); break;
    case TYPE_J: immJ(); src1I(*imm); src2I(s->snpc); break;
    case TYPE_S: immS(); destI(*imm); goto R;
    case TYPE_B: immB(); destI(*imm); R: // fall through
    case TYPE_R: src1R(rs1); src2R(rs2); break;
    case TYPE_N: break;
    default: panic("type = %d at pc = " FMT_WORD , type, s->pc);
  }
}

