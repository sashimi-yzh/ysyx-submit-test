static word_t rvc_imm_internal(uint32_t inst, const char *str, int len, bool sign) {
  word_t imm = 0;
  uint32_t msb_mask = 0;
  uint32_t inst_sll16 = inst << 16;
  assert(len == 16);
#define macro(i) do { \
    char c = str[i]; \
    if (c != '.') { \
      Assert((c >= '0' && c <= '9') || (c >= 'a' && c <= 'f'), \
          "invalid character '%c' in pattern string", c); \
      int pos = (c >= '0' && c <= '9') ? c - '0' : c - 'a' + 10; \
      int nr_srl = 16 + (15 - (i)) - pos; \
      uint32_t mask = 1u << pos ; \
      imm |= (inst_sll16 >> nr_srl) & mask; \
      if (mask > msb_mask) { msb_mask = mask;} \
    } \
  } while (0)

  macro16(0);
#undef macro

  if (sign && (msb_mask & imm)) {
    imm |= (-imm) & ~((word_t)msb_mask - 1);
  }
  return imm;
}

#define rvc_simm(str) *imm = rvc_imm_internal(i, str, STRLEN(str), true)
#define rvc_uimm(str) *imm = rvc_imm_internal(i, str, STRLEN(str), false)
#define rvc_pcimm(str) (rvc_simm(str) + s->pc)
#define creg2reg(creg) (creg + 8)

static void decode_operand_rvc(Decode *s, int *rd_, word_t *src1, word_t *src2, word_t *imm, int type) {
  uint32_t i = s->isa.inst;
  int r9_7 = creg2reg(BITS(i, 9, 7));
  int r4_2 = creg2reg(BITS(i, 4, 2));
  int r11_7 = BITS(i, 11, 7);
  int r6_2 = BITS(i, 6, 2);
#define rdrs1(r0, r1) do { destR(r0); src1R(r1); } while (0)
#define rdrs1_same(r) rdrs1(r, r)
#define rs1rs2(r1, r2) do { src1R(r1); src2R(r2); } while (0)
  switch (type) {
    case TYPE_CIW:    rvc_uimm ("...54987623....."); src2I(*imm);        rdrs1(r4_2, 2);            break;
    case TYPE_CLW:    rvc_uimm ("...543...26....."); src2I(*imm); goto CL;
    case TYPE_CLD:    rvc_uimm ("...543...76....."); src2I(*imm); CL:    rdrs1(r4_2, r9_7);         break;
    case TYPE_CSW:    rvc_uimm ("...543...26....."); destI(*imm); goto CW;
    case TYPE_CSD:    rvc_uimm ("...543...76....."); destI(*imm); CW:    rs1rs2(r9_7, r4_2);        break;
    case TYPE_CI:     rvc_simm ("...5.....43210.."); src2I(*imm);        rdrs1_same(r11_7);         break;
    case TYPE_CASP:   rvc_simm ("...9.....46875.."); src2I(*imm);        rdrs1_same(2);             break;
    case TYPE_CLUI:   rvc_simm ("...5.....43210.."); src1I(*imm << 12);  destR(r11_7);              break;
    case TYPE_CSHIFT: rvc_uimm ("...5.....43210.."); src2I(*imm);        rdrs1_same(r9_7);          break;
    case TYPE_CANDI:  rvc_simm ("...5.....43210.."); src2I(*imm);        rdrs1_same(r9_7);          break;
    case TYPE_CJ:     rvc_pcimm("...b498a673215.."); src1I(*imm);                                   break;
    case TYPE_CB:     rvc_pcimm("...843...76215.."); destI(*imm);        rs1rs2(r9_7, 0);           break;
    case TYPE_CIU:    rvc_uimm ("...5.....43210.."); src2I(*imm);        rdrs1_same(r11_7);         break;
    case TYPE_CLWSP:  rvc_uimm ("...5.....43276.."); src2I(*imm); goto CI_ld;
    case TYPE_CLDSP:  rvc_uimm ("...5.....43876.."); src2I(*imm); CI_ld: rdrs1(r11_7, 2);           break;
    case TYPE_CSWSP:  rvc_uimm ("...543276......."); destI(*imm); goto CSS;
    case TYPE_CSDSP:  rvc_uimm ("...543876......."); destI(*imm); CSS:   rs1rs2(2, r6_2);           break;
#ifdef CONFIG_RVFD
    case TYPE_CFLD:   rvc_uimm ("...543...76....."); src2I(*imm);        destFR(r4_2); src1R(r9_7); break;
    case TYPE_CFLDSP: rvc_uimm ("...5.....43876.."); src2I(*imm);        destFR(r11_7); src1R(2);   break;
    case TYPE_CFSD:   rvc_uimm ("...543...76....."); destI(*imm);        src1R(r9_7); src2FR(r4_2); break;
    case TYPE_CFSDSP: rvc_uimm ("...543876......."); destI(*imm);        src1R(2); src2FR(r6_2);    break;
#endif
    case TYPE_CS: rdrs1_same(r9_7);  src2R(r4_2); break;
    case TYPE_CR: rdrs1_same(r11_7); src2R(r6_2); break;
    case TYPE_N: break; // for ebreak
    default: panic("type = %d at pc = " FMT_WORD , type, s->pc);
  }
}

