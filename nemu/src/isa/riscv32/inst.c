static void record_mtrace(vaddr_t addr, bool is_write) {
  IFNDEF(CONFIG_YSYXSOC, return);
  return;
  static FILE *fp = NULL;
  if (fp == NULL) {
    fp = fopen("mem.trace", "w");
    assert(fp);
  }
  fwrite(&addr, sizeof(addr), 1, fp);
  fwrite(&is_write, sizeof(is_write), 1, fp);
}

enum {
  BTRACE_TYPE_B, // branch
  BTRACE_TYPE_J, // jump
  BTRACE_TYPE_I, // indirect
};
void record_btrace(uint8_t type, vaddr_t pc, vaddr_t target, bool taken) {
  IFNDEF(CONFIG_YSYXSOC, return);
  return;
  static FILE *fp = NULL;
  if (fp == NULL) {
    fp = fopen("branch.trace", "w");
    assert(fp);
  }
  fwrite(&type, sizeof(type), 1, fp);
  fwrite(&pc, sizeof(pc), 1, fp);
  fwrite(&target, sizeof(target), 1, fp);
  fwrite(&taken, sizeof(taken), 1, fp);
}

